=begin
There are a number of variables listed below. What are the different types and how do you know 
which is which?

excited_dog = "excited dog" - method variable
@excited_dog = "excited dog" - instance variable
@@excited_dog = "excited dog" - class variable

they are all named differently eith @'s

LS Solution
Here we have the following variables:

Local variable - excited_dog Instance variable - @excited_dog Class variable - @@excited_dog

We can tell which is which by how the variables are prefixed. Local variables do not contain 
anything prefixed, while instance variables are prefixed with the @ and class variables are 
prefixed with @@.
=end